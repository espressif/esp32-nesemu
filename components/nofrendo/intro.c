static uint8 *intro_getrom(void)
{
   uint8 *rom;
   
   rom = NOFRENDO_MALLOC(CODE_SIZE);   
   if (NULL != rom)
   {
      /* good measure */
      memset(rom, 0, CODE_SIZE);

      /* copy in the code */
      memcpy(rom, intro_code, sizeof(intro_code));

      /* copy in the interrupt vectors */
      memcpy(rom + (CODE_SIZE - sizeof(intro_vec)), intro_vec, sizeof(intro_vec));
   }

   return rom;
}

static uint8 *intro_getvrom(void)
{
   uint8 *vrom;

   vrom = NOFRENDO_MALLOC(VROM_SIZE);
   if (NULL != vrom)
   {
      memcpy(vrom, intro_vrom, sizeof(intro_vrom));
   }

   return vrom;
}

void intro_get_header(rominfo_t *rominfo)
{
   ASSERT(rominfo);

   rominfo->rom_banks = CODE_SIZE / 0x4000;
   rominfo->vrom_banks = VROM_SIZE / 0x2000;
   rominfo->mirror = MIRROR_VERT;
   rominfo->flags = 0;
   rominfo->mapper_number = 0;
   
   rominfo->sram_banks = 0;
   rominfo->vram_banks = 0;
   rominfo->sram = NULL;
   rominfo->vram = NULL;

   strncpy(rominfo->filename, "Nofrendo intro", PATH_MAX);
}

int intro_get_rom(rominfo_t *rominfo)
{
   ASSERT(rominfo);

   rominfo->rom = intro_getrom();
   rominfo->vrom = intro_getvrom();
   if (NULL == rominfo->rom || NULL == rominfo->vrom)
      return -1; 

   return 0;
}

